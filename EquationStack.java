
package hw3;

import java.util.Stack;

/**
 *
 * @author Judah Ben-Eliezer
 *
 */
public class EquationStack extends Stack<String> {

	/**
	 *
	 */
	public EquationStack() {
		super();
	}

	/**
	 *
	 */
	public String push(String s) {
		return super.push(s);
	}
	
	/**
	 *
	 */
	public String pop() {
		return super.pop();
	}
	
	/**
	 *
	 */
	public boolean isEmpty() {
		return super.isEmpty();
	}
	
	/**
	 *
	 */
	public int size() {
		return super.size();
	}
}
